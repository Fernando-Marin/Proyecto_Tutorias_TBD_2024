    private void btnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioActionPerformed
        // Obtener el texto de los campos de correo y contraseña
        String correo = cajaCorreo.getText();
        String contrasena = new String(cajaContrasena.getPassword());

        // Crear una instancia del DAO
        UsuarioDAO usuarioDAO = new UsuarioDAO();

        // Verificar el login
        if (usuarioDAO.login(correo, contrasena)) {
            // Mostrar mensaje de éxito
            VentanaAlumos ventana = new VentanaAlumos();
            ventana.setVisible(true);
            
            dispose();
        } else {
            // Mostrar mensaje de error
            JOptionPane.showMessageDialog(this, "Correo o contraseña incorrectos.", "Error de Login", JOptionPane.ERROR_MESSAGE);
        }      // TODO add your handling code here:
    }//GEN-LAST:event_btnInicioActionPerformed

