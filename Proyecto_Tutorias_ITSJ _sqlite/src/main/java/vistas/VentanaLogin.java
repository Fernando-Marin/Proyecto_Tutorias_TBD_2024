    private void cajaCorreoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cajaCorreoKeyTyped
        // TODO add your handling code here:
        //Se obtiene el caracter ingresado
        char c = evt.getKeyChar();
         if (Character.isUpperCase(c)) {
        // Cancelamos el evento actual (no escribirá la letra mayúscula)
        evt.consume();
        // Insertamos la versión en minúscula del carácter en el JTextField
        cajaCorreo.setText(cajaCorreo.getText() + Character.toLowerCase(c));
    }else if(!Character.isLetterOrDigit(c) && c != '@' && c != '.'){
        //Si el caracter no es letra, digito, . o @ el evento se realiza
        evt.consume();
    }
        
    }//GEN-LAST:event_cajaCorreoKeyTyped

